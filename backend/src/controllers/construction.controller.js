import { Router } from 'express';
import { check, validationResult } from 'express-validator';
import {
  getConstructions,
  createConstruction,
  getConstruction,
  deleteConstruction,
  updateConstruction,
} from '../services/construction.service';
import { handleError, ErrorHandler } from '../helpers/error';
import { AuthMiddleware } from '../middlewares/auth.middleware';

const router = Router();

/**
 * @swagger
 * /constructions:
 *  get:
 *    security:
 *      - cookieAuth: []
 *    summary: List all constructions
 *    responses:
 *      '200':
 *        description: OK
 *    tags:
 *      - constructions
 */
router.get('/', AuthMiddleware, async (req, res) => {
  try {
    const constructions = await getConstructions({});
    return res.status(200).json({
      status: 200,
      constructions,
    });
  } catch (e) {
    handleError(e, res);
  }
});

/**
 * @swagger
 * /constructions:
 *  post:
 *    security:
 *      - cookieAuth: []
 *    summary: Create a new construction
 *    requestBody:
 *      content:
 *        application/json:
 *          schema:
 *            $ref: '#components/schemas/Construction'
 *    responses:
 *      '201':
 *        description: Created
 *      '400':
 *        description: Bad Request
 *    tags:
 *      - constructions
 */
router.post(
  '/',
  [
    check('name').not().isEmpty().withMessage('Name is missing'),
    check('location').not().isEmpty().withMessage('Location is missing'),
  ],
  AuthMiddleware,
  async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json(errors.array());
    }

    try {
      const construction = await createConstruction(req.body);
      return res.status(201).json({
        status: 201,
        construction,
      });
    } catch (e) {
      handleError(e, res);
    }
  }
);

/**
 * @swagger
 * /constructions/{id}:
 *  get:
 *    security:
 *      - cookieAuth: []
 *    summary: Get a construction's details
 *    parameters:
 *      - in: path
 *        name: id
 *        schema:
 *          type: string
 *        required: true
 *        description: ID generated by MongoDB
 *    responses:
 *      '200':
 *        description: OK
 *      '404':
 *        description: Not Found
 *    tags:
 *      - constructions
 */
router.get('/:id', async (req, res) => {
  try {
    const { id } = req.params;
    const construction = await getConstruction(id);
    if (!construction) throw new ErrorHandler(404, 'Construction not found');

    return res.status(200).json({
      status: 200,
      construction,
    });
  } catch (e) {
    handleError(e, res);
  }
});

/**
 * @swagger
 * /constructions/{id}:
 *  delete:
 *    security:
 *      - cookieAuth: []
 *    summary: Delete a construction
 *    parameters:
 *      - in: path
 *        name: id
 *        schema:
 *          type: string
 *        required: true
 *        description: ID generated by MongoDB
 *    responses:
 *      '202':
 *        description: Accepted
 *      '404':
 *        description: Not Found
 *    tags:
 *      - constructions
 */
router.delete('/:id', AuthMiddleware, async (req, res) => {
  try {
    const construction = await deleteConstruction(req.params.id);
    if (!construction) throw new ErrorHandler(404, 'Construction not found');

    return res.status(202).json({
      status: 202,
    });
  } catch (e) {
    handleError(e, res);
  }
});

/**
 * @swagger
 * /constructions/{id}:
 *  put:
 *    security:
 *      - cookieAuth: []
 *    summary: Edit a construction
 *    parameters:
 *      - in: path
 *        name: id
 *        schema:
 *          type: string
 *        required: true
 *        description: ID generated by MongoDB
 *    requestBody:
 *      content:
 *        application/json:
 *          schema:
 *            $ref: '#components/schemas/Construction'
 *    responses:
 *      '200':
 *        description: OK
 *      '400':
 *        description: Bad Request
 *      '404':
 *        description: Not Found
 *    tags:
 *      - constructions
 */
router.put('/:id', AuthMiddleware, async (req, res) => {
  try {
    const { id } = req.params;
    const construction = await updateConstruction(id, req.body);
    if (!construction) throw new ErrorHandler(404, 'Construction not found');

    return res.status(200).json({
      status: 200,
    });
  } catch (e) {
    handleError(e, res);
  }
});

export default router;
